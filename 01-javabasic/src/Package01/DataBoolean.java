package Package01;

public class DataBoolean {
	public static void main(String[] args) {
		/*
		 * boolean 자료형은 참/거짓을 구분할때 사용 
		 * 이자료형의 변수에는 
		 * 오직 true/false만 담을수 있다
		 */
		boolean a = true;
		boolean b = false;
		// boolean c= 0; // 다른언어와 달리 숫자로 표기 금지 / boolean을 int로 바꾸거나 0을 참/거짓으로 바꾸는 방법뿐
		System.out.println(a);
		System.out.println(b);

	}

}
