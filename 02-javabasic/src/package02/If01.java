package package02;

public class If01 {
	public static void main(String[] args) {
		/*
		 * if(조건식){ 
		 *       실행할 구문 1; 
		 *       실행할 구문 2; 
		 *       .... 
		 *       .... 
		 *    }
		 */
		int a = 10;
		if (a > 4) {
			System.out.println("a는 4보다 큽니다. 따라서 true이기 때문에 이 구문이 실행됬습니다.");
		} else {
			System.out.println("a는 4보다 작아서 flase가 나오기때문에 이 구문이 실행 됬습니다.");
		}

	}

}
