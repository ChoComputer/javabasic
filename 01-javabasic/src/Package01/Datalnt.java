package Package01;

public class Datalnt {

	public static void main(String[] args) {
		// 정수형을 나타낼수 있는 자료형은 크게 4가지 입니다.
		// byte, short, int, long 형 있습니다.
		// 변수 선언시 지정자료형을 위의 키워드로 지정합니다
		byte a = 127; // -127~127
		short b = 32000;
		int c = 3;
		long d = 4; // 1 2 4 8 순으로 커진다 2진수
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}
