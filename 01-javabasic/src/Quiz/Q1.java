package Quiz;

/*
 * Quiz 
 * main 메소드 내부에 정수 변수 1 개 / 2개짜리 한줄 선언 (총변수 3개)
 * 세개의 변수에 값들은 초기화
 * System.out.println();으로 세개의 변수 합 출력
 * 
 */
public class Q1 {
	public static void main(String[] args) {
		int a;
		int b, c;
		a = 10;
		b = 25;
		c = 30;
		System.out.println(a + b + c);
		System.out.println(10 + 25 + 30);
	}

}
