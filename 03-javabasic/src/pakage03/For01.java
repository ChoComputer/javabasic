
package pakage03;

public class For01 {
	public static void main(String[] args) {
		// for문은 while문 과는 다르게 몇 바퀴 돌릴지 정해진 상황에서
		// 사용하는 반복문 입니다.
		// 문법은
		// for(시작변수; 종료조건; 증감식){
		//      실행구문;
		//   }
		// 으로 이루어져 있습니다.
		
		
		for(int i= 0; i<2; i++) {
			System.out.println("for를 이용한 반복문 " + i);
			
			// 공식 이라생각 하삼 )) i=0 주고 조건문에 i<숫자 를 넣으면 무조건 숫자만큼 반복됨 0포함 되있기 때문에 
		}
	}

}
